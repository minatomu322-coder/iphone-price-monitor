def quick_analysis_prompt(url: str) -> str:
    """クイック登録用: 商品URLからAIへ渡す解析依頼文を作る。"""
    return "\n".join([
        "以下の仕入れ候補商品を解析してください。",
        f"商品URL: {url}",
        "（このメッセージに商品ページのスクリーンショットまたは本文コピーを添付します）",
        "",
        "やってほしいこと:",
        "1. 商品名・型番・JANコード・ブランド・カテゴリー・状態・付属品・傷や欠品を特定する",
        "2. 特定できない項目は無理に埋めず、確認すべき点として挙げる",
        "3. メルカリの売り切れ相場を調査できる場合は、最安値・中央値・売り切れ件数・販売中件数を調べる"
        "（できない場合はmarketを省略）",
        "4. 偽物や型番違いのリスクがあれば警告する",
        "5. 仕入れの一次見解（買い/見送り等）と自信度（0〜100%）も添える",
        "",
        "最後に、システム取り込み用として以下の形式のJSONを1つコードブロックで出力してください。",
        "確認できなかった項目はJSONから省いてください。",
        '{"type":"mercari_item_info","name":"商品名","model_number":"...","jan_code":"...",'
        '"brand":"...","category":"...","condition":"...","accessories":"...","flaws":"...",'
        '"notes":"注意点",',
        ' "purchase_price":仕入れ価格,"purchase_shipping":仕入れ送料,'
        f'"purchase_url":"{url}","purchase_source":"仕入れ先名",',
        ' "verdict":"買い/条件付きで買い/見送り/追加確認が必要","confidence":自信度0-100,',
        ' "market":{"min_price":売切最安値,"median_price":売切中央値,"sold_count":売切件数,'
        '"active_count":販売中件数,"url":"相場検索URL","notes":"外れ値の扱いなど"}}',
    ])
